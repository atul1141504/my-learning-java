package intruductionToPrimitiveDataTypes;

public class Primitive3NumberSystem {
	public static void main(String[] args) {
		//Octal Number System
		int octNum1 = 010;
		System.out.printf("Octal octNum1:%d",octNum1).println();
		//Hexadecimal Number System
		int hexNum1 = 0x10;
		System.out.printf("Octal hexNum1:%d",hexNum1).println();
	}
}
